print("""1. Составить программу нахождения наибольшего общего делителя (НОД) и 
наименьшего общего кратного (НОК) двух натуральных чисел НОК(А, В) = (A*B)/НОД(A,B). 
Использовать подпрограмму алгоритма Евклида для определения НОД.""")
def euclidean(a, b):
    while b != 0:
        temp_a = a
        a = b
        b = temp_a % b
    return a

A = int(input("Введите первое натуральное число A: "))
B = int(input("Введите второе натуральное число B: "))

if A <= 0 or B <= 0:
    print("Числа должны быть натуральными (больше 0)")
else:
    nod = euclidean(A, B)
    nok = (A * B) // nod
    
    print(f"НОД({A}, {B}) = {nod}")
    print(f"НОК({A}, {B}) = {nok}")
    print(f"Проверка: ({A} * {B}) / {nod} = {nok}")